TEST(ColorsSpacesHsl, randomConversion)
{
    EXPECT_TRUE(RandomPassthrough(HSL, RGB, 1000));
}

TEST(ColorsSpacesHsl, components)
{
    auto c = Manager::get().find(HSL)->getComponents();
    ASSERT_EQ(c.size(), 3);
    ASSERT_EQ(c[0].id, "h");
    ASSERT_EQ(c[1].id, "s");
    ASSERT_EQ(c[2].id, "l");
}

